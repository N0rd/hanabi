<h3>BEVEZETÉS</h3>
	<p> A hanabi eredendően egy kártyajáték, rendszerünk ezt probálja leszimulálni.</p>
	<p>A Hanabi kifejezés a japán nyelvből ered, melynek jelentése tűzvirág. A Hanabiban a játékosok bombasztikus tűzijátékot próbálnak az égre varázsolni. Ehhez mindannyian egy csapatként fognak dolgozni. A játékosoknak csak egyesült erővel sikerülhet a nézőket lenyűgözni és az isteneket megörvendeztetni.</p>

<h3>JÁTÉKELEMEK</h3>
	<p>A játék 50 tűzijátékkártyából áll öt színben (piros, sárga, zöld, kék, fehér), színenként 10-10 kártyával a következő értékekkel:<br />3db 1es, 2db 2es, 2db 3as, 2db 4es, 1db 5ös</p>
	<p>Adott még extra 10 tűzijátékkártya a különböző játékvariánsokhoz, a fentivel megegyező értékekel</p>
	<p>8 utalás jelsző (+1 a variánsokhoz)</p>
	<p>3 vihar jelző</p>
	
<h3>A JÁTÉK CÉLJA</h3>
	<p>A Hanabi egy kooperatív játék, ami azt jelenti, hogy a játékosok egy csapatként játszanak. A játékosoknak a tűzijátékkártyákat színenként kell emelkedő számsorrendben kijátszaniuk, minden szint csak egyszer elkezdeve. Azonban saját kártyáikat nem láthatják, így csak a társaiktól eredő utalásokra támaszkodhatnak. Minél több kártyát tudnak a játékosok hiba nélkül kijátszani, annál	több pontot kapnak a játék végén.</p>
	
<h3>ELŐKÉSZÜLETEK</h3>
	<p>A tűzijátékkártyákat jól megkeverjük. A kártyákból a játékosok számától függően fogunk osztani a játékosoknak:</p>
		<ul> 
			<li>2 vagy 3 játékos esetén: 5 kártyát</li>
	 		<li>4 vagy 5 játékos esetén: 4 kártyát</li>
		</ul>
	<p class="important">Fontos: Más kártyajátékokkal ellentétben itt a játékosok nem láthatják saját kártyáikat! Mindenkinek úgy kell a kártyákat	kézbe vennie, hogy csak a hátlapját lássa. A kártyák előlapját csak a többi játékos láthatja.</p>
	<p>A többi kártyát képpel lefelé fordítva az asztal közepére helyezzük: ez lesz a húzópaklink.</p>

<h3>JÁTÉKMENET</h3>
	<p>Az óramutató járása szerint fogunk játszani. A soron lévő játékosnak a következő akciók egyikét kell végrehajtania. A játékosnak ki kell választania az egyik akciót. Passzolnia nem lehet!	Ha a játékos befejezte akcióját, az óramutató járása szerint következő játékos kerül sorra.</p>
	<ol class="abc">
		<li>Utalást tesz egy másik játékos lapjára</li>
		<li>Eldob egy kártyát vagy</li>
		<li>Kijátszik egy kártyát</li>
	</ol></p>
	<p class="important">Fontos: A többi játékos sem az akció kiválasztását, sem magát az akciót nem kommentálhatja	és semmilyen módon nem befolyásolhatja azt.</p>
<h4>a. Egy utalást adni</h4>
	<p>A játékosok utalások segítségével kaphatnak információt arról, hogy mi van a kezükben. A játékos elhasznál egy utalást, egy másik játékos segitségére. A következő utalások egyikét adhatja:</p>
		<ol>
			<li><h5>Szín megnevezése</h5>
				<p>A játékos megnevez egy színt és megmutatja egy játékostársának az összes ilyen színű kártyáját.</p>
				<p class="important">Fontos: A játékos ilyenkor az <strong>összes</strong> ilyen színű kártyát meg mutatja társának!</p>
				<p class="example">Példa: A sárga kártyáid: ez és ez.</p>
			</li>
			<li><h5>Szín tagadása</h5>
				<p>Megnevezzük azt a szint amivel az adott játékostársunk nem rendelkezik!</p>
				<p class="example">Példa: Nincs kék kártyád.</p>
			</li>
			<li><h5>Érték megnevezése</h5>
				<p>A játékos megnevez egy értéket és megmutatja a játékostársának az összes olyan értékű kártyáját.</p>
				<p class="important">Fontos: A játékos ilyenkor az <strong>összes</strong> olyan értékű kártyát meg megmutatja társának!</p>
				<p class="example">Példa: Egy 5-ösöd van, ez.</p>
			</li>
			<li><h5>Érték tagadás</h5>
				<p>Megnevezzük azt az értéket amivel adott játékostársunk nem rendelkezik!</p>
				<p class="example">Példa: Nincs 2-esed.</p>
			</li>
		</ol>
		<p class="important">Fontos: Ha az összes utaláslapka a fekete oldalával néz felfelé, ezt az akciót már nem	választhatjuk! A játékosoknak a másik két akció közül kell választaniuk.</p>
<h4>b. Egy kártya eldobása</h4>
	<p>Egy kártya eldobásakor egy már ehasznált utalási lehetőséget visszakapnak a játékosok. A játékosnak szólnia kell, hogy eldobni szeretne egy	kártyát . A játékos kezéből egyik kártyáját képpel felfelé leteszi a húzópakli mellé. Ezután húz egy kártyát a húzópakliról és anélkül, hogy megnézné a képét, a kezébe veszi azt.</p>
		<p class="important">Fontos: Ha az összes utaláslapka a fehér oldalával néz felfelé, ezt az akciót már nem választhatjuk! A játékosoknak a másik két akció közül kell választaniuk.</p>
<h4>c. Egy kártya kijátszása</h4>
	<p>A játékosnak szólnia kell, hogy kártyát szeretne kijátszani. A játékos lehelyez az asztalra egy kártyát a kezéből. A kijátszott lappal két dolog történhet:</p>
		<ul>
			<li>Ha illik a tüzijátékba:
					<p>	A játékos behelyezi a kijátszott kártyáját az asztal közepén lévő tűzijátékba	a megfelelő színsorba. A kártyák lehelyezését A tűzijáték bekezdésben részletezzük.</p>
			</li>
			<li>Ha nem illik a tüzijátékba.
				<p>Az isteneket felbőszíti ez a hiba közeleg a vihar, az egyik viharjelző átfordul a villámot	ábrázoló oldalára, a játékosnak pedig képpel felfelé el kell dobnia a kártyát a dobópaklira.</p>
			</li>
		</ul>
		<p>Ezután húz egy kártyát a húzópakliról és anélkül, hogy megnézné a képét, a kezébe veszi azt. <p>
	
	<h3>A TÜZIJÁTÉK</h3>
		<p>A tűzijáték öt különböző színből fog állni. Minden színsort emelkedő	számsorrendben kell felépítenünk 1-től 5-ig. Minden színsort egy 1-es értékű kártyával lehet elkezdeni. Ezután egy színsorhoz úgy illeszthetünk kártyát, hogy	annak pontosan 1-gyel magasabbnak kell lennie, mint az azt megelőző kártya értéke. Egy színsorban nem szerepelhet ugyanaz a szám kétszer. Egyetlen színsort sem lehet töbször megkezdeni.</p>
		<h4>Jutalom</h4>
			<p>Ha egy színsorhoz kijátsszuk az 5-ös	számú kártyát, az adott sor elkészül.	Jutalomból egy  már elhasznált utalásjelző átfordulhat a fehér oldalára. Ha már mindegyik utaláslapka a fehér oldalával néz felfelé, a játékosok nem kapnak jutalmat.</p>
