<h3>VARIÁNSOK</h3>
	<h4>Nehézségi szint módosítása</h4>
	 <p class="rulesp">Túl egyszerű? Játszatok csak 2 vagy akár csak 1 viharjelzővel.</p>
	 <p class="rulesp">Túl nehéz? Használjátok a tartalék utaláslapkát vagy egészítsétek ki érmékkel, melyek további utaláslapkákat helyettesíthetnek.</p>
	<h4>Szín hozzáadás</h4>
		<p class="rulesp">Az előkészületeknél egy 6. színt is keverjetek a többi kártya közé. A maximálisan elérhető pontszám így már 30 pont lesz.</p>
	<h4>Nehezitett szín	hozzáadás</h4>
		<p class="rulesp">Ha nehezíteni szeretnétek, csak 5 extra színű kártyát keverjetek a többi közé: 1db 1, 1db 2, 1db 3, 1db 4, 1db 5.</p>
	<h4>Hamis szín hozzáadás</h4>
		<p class="rulesp">Itt bekeverünk 10 szivárvány szinű tűzijátékkártyát a többi közé, így a játék során lesz egy 6. szín is. A szín-utalásnál azonban nem nevezhetjük őket szivárványnak. Ehelyett a szín-utalásnál a megnevezett színű lapokon felül a tarka kártyákra minden esetben rá kell mutatnunk, mintha azok is adott szintüek lennének.</p>
		<p class="last">Nem állíthatjuk, hogy nincs adott színű lajpja a játékostársunknak, ha van szivárvány lapja, hiszen az "pont" olyan szinű.</p>
